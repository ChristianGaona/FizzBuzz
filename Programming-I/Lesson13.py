def calculate_volume(long, wide, high):
    """
    Calculates the volume of a box expressed in cubic centimeters.
    
    Args:
        long (float): length of the box.
        wide(float): width of the box.
        high (float): box height.
        
    returns:
        float: volume of the box
    """
    return long * wide * high