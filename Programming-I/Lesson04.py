print("Enter a number:")
number = int(input())

# Evaluates if the number is less than 100.
print(number < 100)

# Evaluates if the number is greater than 100.
print(number > 100)

# Evaluates if the number is less than or equal to 100.
print(number <= 100)

# Evaluates if the number is greater than or equal to 100.
print(number >= 100)

# Evaluate if the number is equal to 100.
print(number == 100)

# Evaluate if the number is different from 100.
print(number != 100)