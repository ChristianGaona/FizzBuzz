# We define a function with two parameters.
def sum(a, b):
    return a + b  # Returns the sum of a & b.

def split(a, b):
    return a / b

result1 = sum(10, 5)  # We call the function and pass the values to the parameters.
result2 = split(10, 5)

# Print the results on the screen.
print("The result one is", result1)
print("The result two is", result2)